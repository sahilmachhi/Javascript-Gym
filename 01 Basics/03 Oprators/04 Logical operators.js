// Logical AND (&&)
// The && operator returns true if both operands are true; otherwise, it returns false.

var isSunny = true;
var isWarm = true;

if (isSunny && isWarm) {
  console.log("It's a sunny and warm day!");
} else {
  console.log("The weather is not both sunny and warm.");
};


// Logical OR (||)
// The || operator returns true if at least one of the operands is true.

var isRaining = false;
var hasUmbrella = true;

if (isRaining || hasUmbrella) {
  console.log("You are ready for the rain!");
} else {
  console.log("You might get wet!");
};

// Logical NOT (!) 

var hasCoffee = false;

if (!hasCoffee) {
  console.log("You need a coffee break!");
} else {
  console.log("You already have coffee.");
};



